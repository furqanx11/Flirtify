    public void insertUserLogin(Connection conn, int userId) {
    try {
        String query = "{CALL sp_InsertUserLogin(?, ?, ?)}";
        CallableStatement pstmt = conn.prepareCall(query);
        pstmt.setInt(1, userId);
        pstmt.setString(2, txt_un.getText());
        pstmt.setString(3, txt_pwd.getText());

        pstmt.executeUpdate();
        System.out.println("UserLogin inserted successfully.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e.getMessage(), "Invalid Details", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}
