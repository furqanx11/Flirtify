    public int checkUserLogin(String username, String password) {
        Connection connection = null;
        
       
        
        try {
            // Open a connection
            
            
            // Prepare the SQL statement
            String sql = "SELECT user_id FROM UserLogin WHERE username = ? AND password = ?";
            ps = con.prepareStatement(sql);
            ps.setString(1, username);
            ps.setString(2, password);
            
            // Execute the query
            rs = ps.executeQuery();
            
            // Retrieve the user_id from the result set
            if (rs.next()) {
                user_id = rs.getInt("user_id");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            // Close the resources
            try {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        
        return user_id;
    }
    
    
     
     
